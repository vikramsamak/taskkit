import mongoose from "mongoose";

const noteSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// Create the Note model
const Note = mongoose.models.Note || mongoose.model("Note", noteSchema);

export default Note;
